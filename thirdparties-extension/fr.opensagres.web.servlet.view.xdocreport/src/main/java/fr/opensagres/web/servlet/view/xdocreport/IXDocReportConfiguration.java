package fr.opensagres.web.servlet.view.xdocreport;

import fr.opensagres.xdocreport.core.XDocReportException;
import fr.opensagres.xdocreport.document.IXDocReport;
import fr.opensagres.xdocreport.template.formatter.FieldsMetadata;

/**
 * API to configure an instance of {@link IXDocReport}.
 */
public interface IXDocReportConfiguration
{
    /**
     * Configure the given instance of {@link IXDocReport}. In this method you can for instance initialize
     * {@link FieldsMetadata} of the report by calling {@link IXDocReport#createFieldsMetadata()}.
     * 
     * @param report instance to configure.
     * @throws XDocReportException when some error occurs while the configuration.
     */
    void configure( IXDocReport report )
        throws XDocReportException;

}
